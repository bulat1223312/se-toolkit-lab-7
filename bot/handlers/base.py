# bot/handlers/base.py
def start() -> str:
    return "Добро пожаловать! Я помощник по лабораторным работам. Используйте /help для списка команд."

def help() -> str:
    return """Доступные команды:
/start - приветственное сообщение
/help - эта справка
/health - проверить состояние бэкенда
/scores <лаба> - получить оценки за лабораторную (заглушка)
/labs - список доступных лабораторных (заглушка)"""

def health() -> str:
    return "Состояние бэкенда: заглушка – позже будет реальный запрос к API."

def scores(lab_id: str = None) -> str:
    if lab_id is None:
        return "Пожалуйста, укажите ID лабораторной, например /scores lab-04"
    return f"Оценки для {lab_id}: заглушка (будет получено из API)"

def labs() -> str:
    return "Доступные лабораторные: заглушка (будет получено из API)"